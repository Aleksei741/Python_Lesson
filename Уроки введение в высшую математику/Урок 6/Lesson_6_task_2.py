import numpy as np

# 2. Найдите псевдорешение:
# x + 2y – z = 1
# 3x – 4y = 7
# 8x – 5y + 2z = 12
# 2x – 5z = 7
# 11x +4y – 7z = 15

A = np.array([[1, 2, -1],
              [3, -4, 0],
              [8, -5, 2],
              [2, 0, -5],
              [11, 4, -7]])
B = np.array([1, 7, 12, 7, 15])
X = np.linalg.lstsq(A, B)
print(f'Ответ: x = {X[0][0]}, y = {X[0][1]}, z = {X[0][2]}')
#Ответ: x = 1.139193526466007, y = -0.9049844399605506, z = -0.9009802999097409
